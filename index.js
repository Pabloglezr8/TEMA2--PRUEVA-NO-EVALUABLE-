const numero = prompt();

if (numero === 0) {
    console.log("EL NUMERO ES CERO")
    alert("EL NUMERO ES CERO")
} else if (numero > 0) {
    console.log("EL NUMERO ES POSITIVO")
    alert("EL NUMERO ES POSITIVO")
} else{
    console.log("EL NUMERO ES NEGATIVO")
    alert("EL NUMERO ES NEGATIVO")
}